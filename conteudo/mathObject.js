//PROPRIEDADES:
console.log("Constante de Euler:",Math.E); //Retorna Constante de Euler
console.log("Logaritmo Natural de 2:",Math.LN2); //Retorna Logaritmo Natural de 2
console.log("Logaritmo Natural de 10:",Math.LN10); //Retorna Logaritmo Natural de 10
console.log("Logaritmo E na base 2:",Math.LOG2E); //Retorna Logaritmo de E na base 2
console.log("Logaritmo E na base 10:",Math.LOG10E); //Retorna Logaritmo de E na base 10
console.log("Pi:",Math.PI); //Retorna o valor de PI
